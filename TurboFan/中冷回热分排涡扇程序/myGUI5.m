set(h,'doublebuffer','on');
for kk=1:number
  main(kk,MECHN1,MECHN2,AREA,ALTITUDE,MACH,RC,RF);
  MECHN1=MECHN1-2;
  str=['当前完成',num2str(kk/number*100),'%...'];
        waitbar(kk/number,h,str);
        pause(0.05); 
end
close(h)
